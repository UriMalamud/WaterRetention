% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
dist=str2num(get(handles.edit1,'String'));
rad=str2num(get(handles.edit2,'String'));
form=str2num(get(handles.edit3,'String'));
comp=str2num(get(handles.edit4,'String'));
starM=str2num(get(handles.edit5,'String'));

temp=load('ICE.mat');
ICE=temp.ICE;
temp=load('WATER.mat');
WATER=temp.WATER;
    
Dn=[3 5 7 10 12 20 30 40 75 100 125 150 175 200 225 250 275 300 325 400];
Rn=[1 5 25 50 100 270 600];
Fn=[3 4 5 10];
Cn=[1 2 3];
Mn=[1 2 3 3.6 5 6.4];
[f1,f2,f3,f4,f5]=ndgrid(Dn,Rn,Fn,Cn,Mn);

set(handles.edit12,'String',num2str(interpn([1 5 25 50 100 270 600],[4.189e15 5.236e17 6.545e19 5.236e20 4.189e21 1.072e23 1.538e24],str2num(get(handles.edit2,'String'))),4));
icef=interpn(f1,f2,f3,f4,f5,ICE,dist,rad,form,comp,starM);
set(handles.edit6,'String',num2str(icef*100,4));
waterf=interpn(f1,f2,f3,f4,f5,WATER,dist,rad,form,comp,starM);
set(handles.edit7,'String',num2str(waterf*100,4));
set(handles.edit8,'String',num2str((1-waterf)*100/(1+comp),4));


